import { categorizeTransaction } from './categorize.js';

let _idCounter = 1;
function mkTx(date, description, amount, type) {
  return {
    id: `tx-${_idCounter++}`,
    date: new Date(date),
    description,
    amount,
    type,
    category: categorizeTransaction(description, type),
  };
}

